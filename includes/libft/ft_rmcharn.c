#include "libft.h"

void	ft_rmcharn(char *s, int n)
{
	if (!s)
		return ;
	if (n >= (int)ft_strlen(s) || n < 0)
		return ;
	while (s[n])
	{
		s[n] = s[n + 1];
		n++;
	}
}
