#include "libft.h"

void	ft_rmchar(char *s, char c)
{
    int i;

	if (!s)
		return ;
	i = 0;
    while (s[i])
	{
        if (s[i] == c)
            ft_rmcharn(s, i);
        else
            i++;
	}
}
